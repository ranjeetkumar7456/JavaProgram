package ConstructorExample;

public class ImportantNotesOnJavaConstructors {
/*
    Important Notes on Java Constructors :
    1. Constructors are invoked implicitly when you instantiate objects.

    2. The two rules for creating a constructor are:
       The name of the constructor should be the same as the class.
       A Java constructor must not have a return type.

    3. If a class doesn't have a constructor, the Java compiler automatically
    creates a default constructor during run-time. The default constructor
    initializes instance variables with default values. For example, the int variable will be initialized to 0

    4. Constructor types:
       a. No-Arg Constructor - a constructor that does not accept any arguments
       b. Parameterized constructor - a constructor that accepts arguments
       c. Default Constructor - a constructor that is automatically created by the Java compiler if it is not explicitly defined.

    5. A constructor cannot be abstract or static or final.
    6. A constructor can be overloaded but can not be overridden.

    */
}
